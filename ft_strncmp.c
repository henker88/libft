#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned char *a;
	unsigned char *b;

	a = (unsigned char *)s1;
	b = (unsigned char *)s2;
	while (*a && (*a == *b) && n)
	{
		a++;
		b++;
		n--;
	}
	return (*a - *b);
}
