#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	int		i;
	char	*mem;

	i = 0;
	mem = (char *)malloc(s[i]);
	while (s[i])
	{
		f(s[i]);
		i++;
	}
}
